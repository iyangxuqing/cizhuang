  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    User.getUserData().then(function (userInfos) {
      for (let i in userInfos) {
        let time = new Date(userInfos[i].created * 1000)
        userInfos[i].time = time.Format('yyyy-MM-dd hh:mm:ss')
      }
      this.setData({
        userInfos: userInfos
      })
    }.bind(this))
  },

